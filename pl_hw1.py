def factorial(k):
    result = 1
    for i in range(1, k+1):
        result = result * i
    return result
def euler_sum(n):

    if n == 0:
        return 0

    total = 0

    for k in range(n):
        total = total + 1 / factorial(k)

    return total
print(euler_sum(6))

