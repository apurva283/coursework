import math

def euler_approx(epsilon):

    true_e = math.exp(1)

    total = 0
    k = 0

    while abs(total - true_e) > epsilon:
        total = total + 1 / math.factorial(k)
        k = k + 1

    return total
print(euler_approx(0.0001))

